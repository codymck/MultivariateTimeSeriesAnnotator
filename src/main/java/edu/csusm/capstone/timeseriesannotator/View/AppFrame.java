    public void setChart(ChartPanel p) {
        if(emptyChart.getParent() == jPanel1) {
          jPanel1.remove(emptyChart);  
        }
        jPanel1.add(p);
        this.pack();
        this.validate();
        jPanel1.repaint();
        jPanel1.revalidate();
    }
    
